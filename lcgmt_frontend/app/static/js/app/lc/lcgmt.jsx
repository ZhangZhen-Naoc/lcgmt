function render_lc(data) {
        console.log(data)
        var series = []
        var instrus = []
        var legends = []
        //var median = 7.0082732; //计算中值
        var median = 1284.656826
        for (let instruId in data.obses) {
            let instru = data.instrus[instruId]
            let lengend = `${instru['mission']} ${instru['energy_start']}-${instru['energy_end']} keV`
            if (data.obses[instruId].flux.length != 0)
                legends.push(lengend)
            var obses = data.obses[instruId]
            var instruObses = []
            for (let idx in obses.times) {
                instruObses.push([obses.times[idx], obses.flux[idx]])
            }
            series.push({
                name: lengend,
                data: instruObses,
                type: 'line',
                markLine: {
                    data: [{
                        name: 'Median',
                        yAxis: median
                    }, {
                        name: 'Median*10',
                        yAxis: median * 10,
                    }],
                    silent: true,
                    lineStyle: {
                        color: "red"
                    }
                },
            })
            instrus.push(instruId)

        };
        var myChart = echarts.init(document.getElementById('lc-chart'))
        myChart.setOption({
            title: {
                text: ''
            },
            tooltip: {},
            xAxis: {
                type: "time",
            },
            yAxis: {
                type: "value",
                name: "Flux 1e-11 erg/s/cm2",
                nameLocation: "end",
                show: true,
                axisLine: {
                    show: true,
                },
            },
            series: series,

            legend: {
                data: legends
            }
        })


}

function render_src_table(data) {
    let table = new Tabulator("#srcs", {
        data: data,
        columns: [
          { title: 'ID', field: 'id', sorter: 'number' },
          { title: 'Name', field: 'name', sorter: 'string' },
          { title: 'RA', field: 'ra', sorter: 'number' },
          { title: 'Dec', field: 'dec', sorter: 'number' },
          { title: 'Dataset', field: 'mission', sorter: 'string' },
          { title: 'Available', field: 'available', sorter: 'boolean' },
        ],
        dataTree: true,
        reactiveData: true,
        // height: 300,
        scrollToRowIfVisible: true,
        rowHeight: 30,
        layout: 'fitDataStretch',
      })
    table.on('rowClick', (e, row) => {
        let data = row.getData();
        if (data.available) {
            srcChanged(data.id)
        } else {
            window.alert('This data is not available, Please login');
        }
        // that.clearSelectedRow();
        // that.lastSelectedRow = row;
        // row.select();
        // that.lastSelectedRow = row;
        // that.$emit("rowClick", row);
    })
    return table
}

